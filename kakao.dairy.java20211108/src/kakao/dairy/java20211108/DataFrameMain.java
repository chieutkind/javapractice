package kakao.dairy.java20211108;

import java.util.ArrayList;
import java.util.Comparator;

public class DataFrameMain {
	public static void main(String[] args) {
		//1.DataFrame 만들기
		//boolean, byte, short, char, int, long, float, double, String
		//DTO Class - 가로
		//배열, ArrayList, LinkedList - 세로

		ArrayList<DTO> ar = new ArrayList<>();

		//2.데이터 추가
		DTO dto = new DTO();
		dto.setName("하나");
		dto.setAge(30);
		//마지막에 데이터 추가
		ar.add(dto);

		//새로운 데이터를 만들어서 dto에 대입
		dto = new DTO();
		dto.setName("두리");
		dto.setAge(28);
		//마지막에 데이터 추가
		ar.add(dto);

		dto = new DTO();
		dto.setName("세리");
		dto.setAge(24);
		//마지막에 데이터 추가
		ar.add(dto);

		dto = new DTO();
		dto.setName("네티");
		dto.setAge(22);
		//2번에 데이터 끼워넣기
		ar.add(2, dto);

		//모든 데이터 확인하기
		//List의 toString으 재정의 되어 있어서 모든 데이터의 toString을 하나의
		//문자열로 합쳐서 리턴함 - 데이터를 확인만 하고자 할 때 사용
		System.out.println(ar);

		for(DTO temp:ar) {
			System.out.println(temp);
		}

		System.out.println("-----------------------");
		//데이터 개수 - size()
		//데이터 1개를 가져오는 것은 get(int index)
		int len = ar.size();
		for(int i=0; i<len; i=i+1) {
			System.out.println(ar.get(i));
		}

		//데이터 수정
		//set(int index, Data data)
		//예리의 나이를 22로 수정
		dto = new DTO();
		dto.setName("다아시");
		dto.setAge(33);
		ar.set(3, dto);

		System.out.println("---------------------");
		for(DTO temp : ar) {
			System.out.println(temp);
		}

		//5. 데이터 삭제
		//remove(int index)
		ar.remove(0);
		System.out.println("---------------------");
		for(DTO temp : ar) {
			System.out.println(temp);
		}
		
		//6.데이터 정렬
		//sort( Comparator comparator)
		
		ar.sort(new Comparator<DTO>() {

			@Override
			public int compare(DTO arg0, DTO arg1) {
				// TODO Auto-generated method stub
				return arg0.getName().compareTo(arg1.getName());
			}
			
		});
		
		System.out.println("---------------------");
		for(DTO temp : ar) {
			System.out.println(temp);
		}
		
		

	}
}
